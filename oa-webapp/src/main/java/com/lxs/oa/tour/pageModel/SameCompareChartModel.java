package com.lxs.oa.tour.pageModel;

public class SameCompareChartModel {
	private String type;
	private String yearType;
	private Long personNumValues;
	private Double moneyValues;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getYearType() {
		return yearType;
	}

	public void setYearType(String yearType) {
		this.yearType = yearType;
	}

	public Long getPersonNumValues() {
		return personNumValues;
	}

	public void setPersonNumValues(Long personNumValues) {
		this.personNumValues = personNumValues;
	}

	public Double getMoneyValues() {
		return moneyValues;
	}

	public void setMoneyValues(Double moneyValues) {
		this.moneyValues = moneyValues;
	}

}
